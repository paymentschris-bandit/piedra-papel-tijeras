function normChallenge(text) {
  return text
    .toLowerCase()
    .replace(/\{ganador\}/g, "G")
    .replace(/\s+/g, " ")
    .trim();
}

function insertPlace(stem, place) {
  if (stem.includes("{lugar}")) return stem.replace(/\{lugar\}/g, place);
  const idx = stem.indexOf("{ganador}");
  if (idx >= 0) {
    const after = stem.slice(idx + "{ganador}".length);
    if (/^\s*[,.]/.test(after)) return stem.replace("{ganador}", `{ganador} ${place}`);
    return stem.replace("{ganador}", `{ganador} ${place}`);
  }
  return `${place.charAt(0).toUpperCase() + place.slice(1)}: ${stem}`;
}

export function generateMegaChallenges(bucket, intensity, mode, minCount = 310) {
  const bank = STEM_BANKS[bucket]?.[intensity];
  if (!bank?.length) return [];
  const places = PLACES_BY_MODE[mode] || PLACES_BY_MODE.home;
  const seen = new Set();
  const out = [];
  for (const stem of bank) {
    for (const place of places) {
      const text = insertPlace(stem, place);
      const key = normChallenge(text);
      if (!seen.has(key)) {
        seen.add(key);
        out.push(text);
      }
    }
  }
  if (out.length < minCount) {
    throw new Error(
      `generateMegaChallenges(${bucket}, ${intensity}, ${mode}): solo ${out.length} retos únicos (mínimo ${minCount})`
    );
  }
  return out;
}

export function validateChallenge(text, bucket) {
  const reason = incompatibleReason(text, bucket);
  if (reason) return { valid: false, reason };

  const a = analyzeText(text);
  if (bucket === "chica_chica" && a.femaleAnat && !a.maleAnat) return { valid: true };
  if (bucket === "chico_chico" && a.maleAnat && !a.femaleAnat) return { valid: true };
  if (bucket.startsWith("hetero")) return { valid: true };

  const classified = classifyChallenge(text, bucket, false);
  if (classified === bucket) return { valid: true };
  return { valid: false, reason: `clasificado como ${classified}` };
}

export function buildAllMegaTrees(mode) {
  const intensities = ["suave", "picante", "extremo"];
  const tree = {};
  for (const intensity of intensities) {
    tree[intensity] = {
      hetero: {
        chica: generateMegaChallenges("hetero.chica", intensity, mode),
        chico: generateMegaChallenges("hetero.chico", intensity, mode),
      },
      chico_chico: generateMegaChallenges("chico_chico", intensity, mode),
      chica_chica: generateMegaChallenges("chica_chica", intensity, mode),
    };
  }
  return tree;
}

export function getStemCounts() {
  const counts = {};
  for (const [bucket, byInt] of Object.entries(STEM_BANKS)) {
    counts[bucket] = {};
    for (const [intensity, stems] of Object.entries(byInt)) {
      counts[bucket][intensity] = stems.length;
    }
  }
  return counts;
}
